/**
 *op_add - Addition
 *@a: First int
 *@b: Second int
 *
 *Return: a + b;
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - Subtraction
 *@a: First int
 *@b: Second int
 *
 *Return: a - b;
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - Multiplication
 *@a: First int
 *@b: Second int
 *
 *Return: a * b;
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - Division
 *@a: First int
 *@b: Second int
 *
 *Return: a / b;
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - Modulus
 *@a: First int
 *@b: Second int
 *
 *Return: a % b;
 */
int op_mod(int a, int b)
{
	return (a % b);
}
